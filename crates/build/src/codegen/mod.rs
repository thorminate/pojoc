/// Byte size of `ty` on the wire when it is a statically fixed-width type, else
/// `None`. Backed by the shared [`type_info`] classifier (`WireSize::Fixed`).
pub(crate) fn wire_fixed_size(ty: &ResolvedTypeRef) -> Option<usize> {
    match type_info(ty).wire_size {
        WireSize::Fixed(n) => Some(n),
        WireSize::Variable => None,
    }
}

fn target_is_lazy(schema: &ResolvedSchema, target_name: &str) -> bool {
    schema
        .versions
        .last()
        .unwrap()
        .fields
        .iter()
        .find(|f| f.name == *target_name)
        .map(|f| f.lazy)
        .unwrap_or(false)
}

/// Whether decode can read this version-field through the single-bounds-checked
/// fixed block using unchecked accessors: it must be statically fixed-width,
/// `Copy`-representable (no `FixedMap`), and a direct read — a non-lazy
/// pass-through or a discard/skip. Casts and lazy fields fall back to the normal
/// per-read checked path.
pub(crate) fn is_blastable(schema: &ResolvedSchema, fl: &FieldLineage) -> bool {
    if wire_fixed_size(&fl.source_ty).is_none() || contains_fixed_map(&fl.source_ty) {
        return false;
    }
    match &fl.mapping {
        FieldMapping::Discard => true,
        FieldMapping::PassThrough { target_name } => !target_is_lazy(schema, target_name),
        FieldMapping::Cast { .. } => false,
    }
}

/// The version's wire fields, stably reordered so that all "blastable"
/// fixed-width fields form a contiguous leading block and everything else
/// follows in declaration order. Encode/decode/skip all consume this identical
/// order, so the reordering is invisible on the wire. Optional fields (always
/// variable) keep their relative order, so optional-header bit indices are
/// unchanged.
pub(crate) fn ordered_wire_fields<'a>(
    schema: &ResolvedSchema,
    vl: &'a VersionLineage,
) -> Vec<&'a FieldLineage> {
    let mut fields: Vec<&FieldLineage> = vl.fields.iter().collect();
    fields.sort_by_key(|fl| u8::from(!is_blastable(schema, fl)));
    fields
}

/// Entrypoint of the codegen, runs after analysis.
pub fn generate(schema: &ResolvedSchema) -> String {
    let mut w = CodeWriter::default();

    // header for the file.
    w.line("// @generated — do not edit");
    w.line("// generated by pojoc");
    w.blank();
    w.line("pub mod runtime { pub use pojoc::*; }");
    w.line("use runtime::*;");
    if cfg!(feature = "serde") {
        w.line("#[allow(unused_imports)]");
        w.line("use serde::{Serialize, Deserialize};");
    }
    w.blank();

    // Types that need a `<'buf>` lifetime: those holding a `lazy` field or a
    // borrowed `&'buf str` (directly or transitively). `type_info` renders these
    // names with `<'buf>` wherever they nest, via the thread-local set installed
    // below. The set also carries module-qualified imported roots that are
    // infected (e.g. `player::Player`), so imported fields carry the lifetime.
    let infected = compute_lifetime_infected(schema);
    let mut infected_types = infected.clone();
    for (alias, imported) in &schema.imports {
        let imp_inf = compute_lifetime_infected(imported);
        if imp_inf.contains(imported.name_hint.as_str()) {
            infected_types.insert(format!("{}::{}", alias.to_snake_case(), imported.name_hint));
        }
    }
    // Install our set; nested `generate` calls for imported submodules swap in
    // their own set and restore ours on return, so ordering is safe.
    let prev = set_infected(infected_types);

    // Types whose `write_*`/`encode_vN` function must return
    // `PojocResult<()>` because they carry (or transitively contain) a
    // `min:`/`max:` constrained field. Same thread-local-set pattern as
    // lifetime infection above, driving a different codegen decision.
    let constraint_infected = compute_constraint_infected(schema);
    let prev_constraint = set_constraint_infected(constraint_infected.clone());

    // Types whose `write_*`/`read_*` function threads the message's shared
    // string-interning table, because they carry (or transitively contain)
    // an `intern`-marked field.
    let intern_infected = compute_intern_infected(schema);
    let prev_intern = set_intern_infected(intern_infected);

    emit_imported_submodules(schema, &mut w);

    structs::emit_enums(schema, &mut w);
    structs::emit_structs(schema, &infected, &mut w);
    structs::emit_bitsets(schema, &mut w);
    structs::emit_unions(schema, &mut w);
    decode::emit_decode_functions(schema, &infected, &mut w);
    emit_dispatcher(
        schema,
        &infected,
        constraint_infected.contains(&schema.name_hint),
        &mut w,
    );
    w.blank();
    encode::emit_size_hint_helpers(schema, &mut w);
    encode::emit_size_hint_vn_functions(schema, &infected, &mut w);
    encode::emit_encode_helpers(schema, &mut w);
    encode::emit_encode_vn_functions(schema, &infected, &mut w);

    let result = w.finish();
    set_infected(prev);
    set_constraint_infected(prev_constraint);
    set_intern_infected(prev_intern);
    result
}

fn emit_dispatcher(
    schema: &ResolvedSchema,
    infected: &HashSet<String>,
    root_constrained: bool,
    w: &mut CodeWriter,
) {
    let name = &schema.name_hint;
    let latest = schema.lineage.latest_version;
    let needs_lifetime = infected.contains(name.as_str());

    let decode_sig = if needs_lifetime {
        format!("pub fn decode(buf: &[u8]) -> PojocResult<{name}<'_>> {{")
    } else {
        format!("pub fn decode(buf: &[u8]) -> PojocResult<{name}> {{")
    };
    w.line(&decode_sig);
    w.indent();
    w.line("let mut pos = 0;");
    w.line("let envelope = read_envelope(buf, &mut pos)?;");
    w.line("let payload = envelope.payload;");
    w.line("let mut pos = 0;");
    w.line("match envelope.version {");
    w.indent();
    for vl in &schema.lineage.versions {
        let v = vl.version;
        w.line(&format!("{v} => decode_v{v}(payload, &mut pos),"));
    }
    w.line("v => Err(Error::UnsupportedVersion(v)),");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();

    let ref_lt = if needs_lifetime { "<'_>" } else { "" };
    let encode_sig = if root_constrained {
        format!("pub fn encode(buf: &mut Vec<u8>, value: &{name}{ref_lt}) -> PojocResult<()> {{")
    } else {
        format!("pub fn encode(buf: &mut Vec<u8>, value: &{name}{ref_lt}) {{")
    };
    w.line(&encode_sig);
    w.indent();
    w.line(&format!("buf.reserve(size_hint_v{latest}(value));"));
    w.line(&format!(
        "let len_pos = write_envelope_header(buf, {latest});"
    ));
    w.line("let payload_start = buf.len();");
    if root_constrained {
        w.line(&format!("encode_v{latest}(buf, value)?;"));
    } else {
        w.line(&format!("encode_v{latest}(buf, value);")); // ← was encode_payload
    }
    w.line("let payload_len = buf.len() - payload_start;");
    w.line("patch_envelope_length(buf, len_pos, payload_len);");
    if root_constrained {
        w.line("Ok(())");
    }
    w.dedent();
    w.line("}");
    w.blank();

    w.line(&format!(
        "pub fn encode_for_version(buf: &mut Vec<u8>, value: &{name}{ref_lt}, version: u64) -> PojocResult<()> {{"
    ));
    w.indent();
    w.line("let hint = match version {");
    w.indent();
    for vl in &schema.lineage.versions {
        let v = vl.version;
        w.line(&format!("{v} => size_hint_v{v}(value),"));
    }
    w.line("v => return Err(Error::UnsupportedVersion(v)),");
    w.dedent();
    w.line("};");
    w.line("buf.reserve(hint);");
    w.line("let len_pos = write_envelope_header(buf, version);");
    w.line("let payload_start = buf.len();");
    w.line("match version {");
    w.indent();
    for vl in &schema.lineage.versions {
        let v = vl.version;
        if root_constrained {
            w.line(&format!("{v} => encode_v{v}(buf, value)?,"));
        } else {
            w.line(&format!("{v} => encode_v{v}(buf, value),"));
        }
    }
    w.line("_ => unreachable!(),");
    w.dedent();
    w.line("}");
    w.line("let payload_len = buf.len() - payload_start;");
    w.line("patch_envelope_length(buf, len_pos, payload_len);");
    w.line("Ok(())");
    w.dedent();
    w.line("}");
    w.blank();

    w.line("pub fn supported_versions() -> &'static [u64] {");
    w.indent();
    let versions: Vec<String> = schema
        .lineage
        .versions
        .iter()
        .map(|vl| vl.version.to_string())
        .collect();
    w.line(&format!("&[{}]", versions.join(", ")));
    w.dedent();
    w.line("}");
}

fn emit_default(default: &DefaultValue, schema: &ResolvedSchema) -> String {
    match default {
        DefaultValue::None => "None".to_string(),
        DefaultValue::Bool(b) => b.to_string(),
        DefaultValue::Int(i) => i.to_string(),
        DefaultValue::Float(f) => {
            if f.fract() == 0.0 {
                format!("{f:.1}")
            } else {
                f.to_string()
            }
        }
        DefaultValue::Str(s) => format!("{s:?}"),
        DefaultValue::Array(els) => {
            if els.is_empty() {
                "PojocVec::new()".to_string()
            } else {
                let pushes = els
                    .iter()
                    .map(|e| format!("__v.push({});", emit_default(e, schema)))
                    .collect::<Vec<_>>()
                    .join(" ");
                format!("{{ let mut __v = PojocVec::new(); {pushes} __v }}")
            }
        }
        DefaultValue::Repeat(_) => unreachable!(
            "DefaultValue::Repeat should always get expanded into DefaultValue::Array before codegen."
        ),
        DefaultValue::Map(pairs) => {
            if pairs.is_empty() {
                "PojocMap::default()".to_string()
            } else {
                let inserts = pairs
                    .iter()
                    .map(|(k, v)| {
                        format!(
                            "__m.insert({}, {});",
                            emit_default(k, schema),
                            emit_default(v, schema)
                        )
                    })
                    .collect::<Vec<_>>()
                    .join(" ");
                format!("{{ let mut __m = PojocMap::default(); {inserts} __m }}")
            }
        }
        DefaultValue::Struct => "Default::default()".to_string(),
        DefaultValue::FixedBytes(bytes) => {
            if bytes.iter().all(|b| b.is_ascii_graphic() || *b == b' ') {
                let s = std::str::from_utf8(bytes).unwrap();
                format!("*b\"{s}\"")
            } else {
                let inner = bytes
                    .iter()
                    .map(|b| format!("0x{b:02x}u8"))
                    .collect::<Vec<_>>()
                    .join(", ");
                format!("[{inner}]")
            }
        }
        DefaultValue::Tuple(elements) => {
            let inner = elements
                .iter()
                .map(|e| emit_default(e, schema))
                .collect::<Vec<_>>()
                .join(", ");
            format!("({inner})")
        }
        DefaultValue::EnumVariant { ty_name, variant } => {
            format!("{ty_name}::{variant}")
        }
        DefaultValue::BitsetLiteral { ty_name, kvs } => {
            if let Some((_, resolved_bitset)) = schema
                .bitsets
                .bitsets
                .iter()
                .filter(|(id, _)| id.name == *ty_name)
                .max_by_key(|(id, _)| id.version)
            {
                let computed_len = resolved_bitset.variants.len().div_ceil(8);
                let mut default_bytes = vec![0u8; computed_len];
                for (flag_name, flag_val) in kvs {
                    if *flag_val
                        && let Some(idx) = resolved_bitset
                            .variants
                            .iter()
                            .position(|v| v.name == *flag_name)
                    {
                        default_bytes[idx / 8] |= 1 << (idx % 8);
                    }
                }
                let inner = default_bytes
                    .iter()
                    .map(|b| format!("0x{b:02x}"))
                    .collect::<Vec<_>>()
                    .join(", ");
                format!("{ty_name}([{inner}])")
            } else {
                format!("{ty_name}::default()")
            }
        }
    }
}

fn get_latest_versions<K, V>(
    items: &HashMap<K, V>,
    get_key_name: impl Fn(&K) -> String,
    get_version: impl Fn(&K) -> i128,
) -> HashMap<String, (i128, &V)>
where
    K: std::hash::Hash + Eq,
{
    let mut latest = HashMap::new();
    for (id, value) in items {
        let version = get_version(id);
        let name = get_key_name(id);
        let entry = latest.entry(name).or_insert((version, value));
        if version > entry.0 {
            *entry = (version, value);
        }
    }
    latest
}

/// A field that (directly or nested) carries a borrowed `&'buf str` forces its
/// container to carry the `'buf` lifetime — exactly like a `lazy` field does.
/// Only meaningful under [`borrow_strings`]; named-type nesting is handled by
/// the propagation loop below via [`field_type_name`].
pub(crate) fn field_carries_borrowed_string(ty: &ResolvedTypeRef) -> bool {
    match ty {
        ResolvedTypeRef::Scalar(id) => normalize_type(&id.name) == "string",
        ResolvedTypeRef::Array(inner)
        | ResolvedTypeRef::FixedArray(inner, _)
        | ResolvedTypeRef::Optional(inner)
        | ResolvedTypeRef::Boxed(inner)
        | ResolvedTypeRef::Constrained { inner, .. }
        | ResolvedTypeRef::Interned(inner) => field_carries_borrowed_string(inner),
        ResolvedTypeRef::Tuple(elems) => elems.iter().any(field_carries_borrowed_string),
        ResolvedTypeRef::Map(k, v) | ResolvedTypeRef::FixedMap(k, v, _) => {
            field_carries_borrowed_string(k) || field_carries_borrowed_string(v)
        }
        _ => false,
    }
}

fn compute_lifetime_infected(schema: &ResolvedSchema) -> HashSet<String> {
    let latest = get_latest_versions(&schema.types.types, |id| id.name.clone(), |id| id.version);

    let mut infected: HashSet<String> = latest
        .iter()
        .filter(|(_, (_, resolved))| {
            resolved
                .fields
                .iter()
                .any(|f| f.lazy || field_carries_borrowed_string(&f.ty))
        })
        .map(|(name, _)| name.clone())
        .collect();

    if schema
        .versions
        .last()
        .map(|v| {
            v.fields
                .iter()
                .any(|f| f.lazy || field_carries_borrowed_string(&f.ty))
        })
        .unwrap_or(false)
    {
        infected.insert(schema.name_hint.clone());
    }

    loop {
        let mut changed = false;
        for (name, (_, resolved)) in &latest {
            if infected.contains(name) {
                continue;
            }
            let newly_infected = resolved.fields.iter().any(|f| {
                let mut names = Vec::new();
                field_type_names(&f.ty, &mut names);
                names.iter().any(|n| infected.contains(n))
            });
            if newly_infected {
                infected.insert(name.clone());
                changed = true;
            }
        }
        if !changed {
            break;
        }
    }

    // The schema root isn't a member of `latest` (it's synthesized, not a
    // `type` declaration), so it never went through the propagation loop
    // above — only the one-time direct-field seed check before it. A root
    // field that only becomes infected *transitively* (e.g. `head: Node?`
    // where `Node` carries a string several hops down, but the root has no
    // string field of its own) would otherwise be missed here.
    if !infected.contains(&schema.name_hint)
        && schema
            .versions
            .last()
            .map(|v| {
                v.fields.iter().any(|f| {
                    let mut names = Vec::new();
                    field_type_names(&f.ty, &mut names);
                    names.iter().any(|n| infected.contains(n))
                })
            })
            .unwrap_or(false)
    {
        infected.insert(schema.name_hint.clone());
    }

    infected
}

/// Whether `ty` is itself a `min:`/`max:` constrained type, unwrapping
/// `Optional`/`Boxed` first (`age: u32(min:0,max:150)?` and
/// `age: box<u32(min:0,max:150)>` both still need a guard where the value is
/// actually read/written).
fn field_is_constrained(ty: &ResolvedTypeRef) -> bool {
    match ty {
        ResolvedTypeRef::Constrained { .. } => true,
        ResolvedTypeRef::Optional(inner) | ResolvedTypeRef::Boxed(inner) => {
            field_is_constrained(inner)
        }
        _ => false,
    }
}

/// Names of types whose `write_*`/`encode_vN` function must return
/// `PojocResult<()>`: those with a directly `min:`/`max:`-constrained field,
/// or that transitively contain a field of another such type (needed purely
/// to propagate the `?` from the inner call up through the outer one).
fn compute_constraint_infected(schema: &ResolvedSchema) -> HashSet<String> {
    let latest = get_latest_versions(&schema.types.types, |id| id.name.clone(), |id| id.version);

    let mut infected: HashSet<String> = latest
        .iter()
        .filter(|(_, (_, resolved))| resolved.fields.iter().any(|f| field_is_constrained(&f.ty)))
        .map(|(name, _)| name.clone())
        .collect();

    let root_seed = |fields: &[FieldIR]| fields.iter().any(|f| field_is_constrained(&f.ty));
    if schema
        .versions
        .last()
        .map(|v| root_seed(&v.fields))
        .unwrap_or(false)
    {
        infected.insert(schema.name_hint.clone());
    }

    loop {
        let mut changed = false;
        for (name, (_, resolved)) in &latest {
            if infected.contains(name) {
                continue;
            }
            let newly_infected = resolved.fields.iter().any(|f| {
                let mut names = Vec::new();
                field_type_names(&f.ty, &mut names);
                names.iter().any(|n| infected.contains(n))
            });
            if newly_infected {
                infected.insert(name.clone());
                changed = true;
            }
        }
        if !changed {
            break;
        }
    }

    if !infected.contains(&schema.name_hint)
        && schema
            .versions
            .last()
            .map(|v| {
                v.fields.iter().any(|f| {
                    let mut names = Vec::new();
                    field_type_names(&f.ty, &mut names);
                    names.iter().any(|n| infected.contains(n))
                })
            })
            .unwrap_or(false)
    {
        infected.insert(schema.name_hint.clone());
    }

    infected
}

/// Emits a `min`/`max` bound-check guard for `field` if its type is
/// `Constrained` — directly, or under one layer of `Optional` (e.g.
/// `age: u32(min:0,max:150)?`). `accessor` must be a plain place expression
/// denoting the field's current value (`__value.foo` for encode, `foo` for a
/// freshly-bound decode local) — this function adds any `&`/deref itself.
/// Checked once against the field's *current* (latest-schema) type, so it
/// applies uniformly across every historical version's encode/decode.
pub(crate) fn emit_constraint_guard(field: &FieldIR, accessor: &str, w: &mut CodeWriter) {
    match &field.ty {
        ResolvedTypeRef::Constrained { inner, min, max } => {
            emit_bound_check(inner, accessor, false, &field.name, *min, *max, w);
        }
        ResolvedTypeRef::Optional(boxed) => {
            if let ResolvedTypeRef::Constrained { inner, min, max } = boxed.as_ref() {
                w.line(&format!("if let Some(__cval) = &{accessor} {{"));
                w.indent();
                emit_bound_check(inner, "__cval", true, &field.name, *min, *max, w);
                w.dedent();
                w.line("}");
            }
        }
        _ => {}
    }
}

#[allow(clippy::too_many_arguments)]
fn emit_bound_check(
    inner: &ResolvedTypeRef,
    accessor: &str,
    is_ref: bool,
    field_name: &str,
    min: Option<f64>,
    max: Option<f64>,
    w: &mut CodeWriter,
) {
    if min.is_none() && max.is_none() {
        return;
    }
    let is_count = matches!(
        inner,
        ResolvedTypeRef::Array(_) | ResolvedTypeRef::DeltaArray(_) | ResolvedTypeRef::Map(_, _)
    ) || matches!(inner, ResolvedTypeRef::Scalar(id) if normalize_type(&id.name) == "string");

    let value_expr = if is_count {
        format!("({accessor}.len() as f64)")
    } else if is_ref {
        format!("(*{accessor} as f64)")
    } else {
        format!("({accessor} as f64)")
    };

    let mut conds = Vec::new();
    if let Some(mn) = min {
        conds.push(format!("{value_expr} < {mn}f64"));
    }
    if let Some(mx) = max {
        conds.push(format!("{value_expr} > {mx}f64"));
    }
    let min_expr = min
        .map(|m| format!("Some({m}f64)"))
        .unwrap_or_else(|| "None".into());
    let max_expr = max
        .map(|m| format!("Some({m}f64)"))
        .unwrap_or_else(|| "None".into());

    w.line(&format!(
        "if {} {{ return Err(Error::ConstraintViolation {{ field: \"{field_name}\", min: {min_expr}, max: {max_expr} }}); }}",
        conds.join(" || ")
    ));
}

/// Names of types whose `write_*`/`read_*` function must thread the shared
/// string-interning table: those with a field whose type is (or transitively
/// contains) `Interned`, or that transitively contain a field of another such
/// type (same containment/fixpoint shape as `compute_constraint_infected`).
fn compute_intern_infected(schema: &ResolvedSchema) -> HashSet<String> {
    let latest = get_latest_versions(&schema.types.types, |id| id.name.clone(), |id| id.version);

    let mut infected: HashSet<String> = latest
        .iter()
        .filter(|(_, (_, resolved))| resolved.fields.iter().any(|f| contains_interned(&f.ty)))
        .map(|(name, _)| name.clone())
        .collect();

    if schema
        .versions
        .last()
        .map(|v| v.fields.iter().any(|f| contains_interned(&f.ty)))
        .unwrap_or(false)
    {
        infected.insert(schema.name_hint.clone());
    }

    loop {
        let mut changed = false;
        for (name, (_, resolved)) in &latest {
            if infected.contains(name) {
                continue;
            }
            let newly_infected = resolved.fields.iter().any(|f| {
                let mut names = Vec::new();
                field_type_names(&f.ty, &mut names);
                names.iter().any(|n| infected.contains(n))
            });
            if newly_infected {
                infected.insert(name.clone());
                changed = true;
            }
        }
        if !changed {
            break;
        }
    }

    if !infected.contains(&schema.name_hint)
        && schema
            .versions
            .last()
            .map(|v| {
                v.fields.iter().any(|f| {
                    let mut names = Vec::new();
                    field_type_names(&f.ty, &mut names);
                    names.iter().any(|n| infected.contains(n))
                })
            })
            .unwrap_or(false)
    {
        infected.insert(schema.name_hint.clone());
    }

    infected
}

/// Whether version `N`'s own wire format (its fields' *historical* types,
/// not the latest struct's) ever touches interned data. Used to gate the
/// root `encode_vN`/`decode_vN` intern-table header: unlike helper readers
/// for named struct types (which always represent the latest shape, so a
/// simple by-name check is correct there), a version's *own* field set can
/// predate every interned field the schema has grown since — e.g. `intern`
/// added fresh in v5 must not retroactively put a table on v1..v4's wire
/// format, or genuinely old v1 bytes fail to decode against today's
/// generated `decode_v1`, which is exactly the breakage schema versioning
/// exists to prevent.
pub(crate) fn version_uses_intern(schema: &ResolvedSchema, fields: &[FieldLineage]) -> bool {
    let mut visited = HashSet::new();
    fields
        .iter()
        .any(|f| type_ref_uses_intern(schema, &f.source_ty, &mut visited))
}

fn type_ref_uses_intern(
    schema: &ResolvedSchema,
    ty: &ResolvedTypeRef,
    visited: &mut HashSet<TypeId>,
) -> bool {
    match ty {
        ResolvedTypeRef::Interned(_) => true,
        ResolvedTypeRef::Array(inner)
        | ResolvedTypeRef::FixedArray(inner, _)
        | ResolvedTypeRef::DeltaArray(inner)
        | ResolvedTypeRef::FixedDeltaArray(inner, _)
        | ResolvedTypeRef::Optional(inner)
        | ResolvedTypeRef::Boxed(inner)
        | ResolvedTypeRef::Constrained { inner, .. } => {
            type_ref_uses_intern(schema, inner, visited)
        }
        ResolvedTypeRef::Tuple(elems) => elems
            .iter()
            .any(|e| type_ref_uses_intern(schema, e, visited)),
        ResolvedTypeRef::Map(k, v) | ResolvedTypeRef::FixedMap(k, v, _) => {
            type_ref_uses_intern(schema, k, visited) || type_ref_uses_intern(schema, v, visited)
        }
        // A struct-typed reference points at one *specific* historical
        // shape (TypeId carries the version) — recurse into exactly that
        // shape's own fields, not whatever the latest version looks like.
        ResolvedTypeRef::Scalar(id) if !is_primitive(&id.name) => {
            if !visited.insert(id.clone()) {
                return false;
            }
            schema.types.types.get(id).is_some_and(|resolved| {
                resolved
                    .fields
                    .iter()
                    .any(|f| type_ref_uses_intern(schema, &f.ty, visited))
            })
        }
        _ => false,
    }
}

/// Names of infected named types this field transitively references, so a
/// struct/map/tuple holding an infected named type is itself infected. (Direct
/// borrowed strings are handled separately by `field_carries_borrowed_string`.)
fn field_type_names(ty: &ResolvedTypeRef, out: &mut Vec<String>) {
    match ty {
        ResolvedTypeRef::Scalar(id) if !is_primitive(&id.name) => out.push(id.name.clone()),
        ResolvedTypeRef::Array(inner)
        | ResolvedTypeRef::FixedArray(inner, _)
        | ResolvedTypeRef::DeltaArray(inner)
        | ResolvedTypeRef::FixedDeltaArray(inner, _)
        | ResolvedTypeRef::Optional(inner)
        | ResolvedTypeRef::Boxed(inner)
        | ResolvedTypeRef::Constrained { inner, .. }
        | ResolvedTypeRef::Interned(inner) => field_type_names(inner, out),
        ResolvedTypeRef::Tuple(elems) => elems.iter().for_each(|e| field_type_names(e, out)),
        ResolvedTypeRef::Map(k, v) | ResolvedTypeRef::FixedMap(k, v, _) => {
            field_type_names(k, out);
            field_type_names(v, out);
        }
        _ => {}
    }
}

fn emit_imported_submodules(schema: &ResolvedSchema, w: &mut CodeWriter) {
    let mut aliases: Vec<&String> = schema.imports.keys().collect();
    aliases.sort(); // deterministic output

    for alias in aliases {
        let imported = &schema.imports[alias];
        let module = alias.to_snake_case();
        let body = generate(imported);

        w.line(&format!("pub mod {module} {{"));
        w.indent();
        for line in body.lines() {
            if line.is_empty() {
                w.blank();
            } else {
                w.line(line);
            }
        }
        w.dedent();
        w.line("}");
        w.blank();
    }
}
